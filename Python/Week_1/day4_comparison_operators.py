# Day 4 - Comparison Operators in Python

a = 10
b = 5

print("a == b:", a == b)   # False
print("a != b:", a != b)   # True
print("a > b:", a > b)     # True
print("a < b:", a < b)     # False
print("a >= 10:", a >= 10) # True
print("a <= 5:", a <= 5)   # False

# Comparing strings
name1 = "Ali"
name2 = "Ali"
print("name1 == name2:", name1 == name2)   # True - Python compares actual text