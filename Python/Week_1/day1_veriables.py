# Day 1 - Understanding Variables in Python

# Integer (whole number)
student_age = 19

# Float (decimal number)
student_gpa = 3.75

# String (text)
student_name = "Ali"

# Boolean (True/False)
is_enrolled = True

# Printing them out with their types
print("Name:", student_name, "| Type:", type(student_name))
print("Age:", student_age, "| Type:", type(student_age))
print("GPA:", student_gpa, "| Type:", type(student_gpa))
print("Enrolled:", is_enrolled, "| Type:", type(is_enrolled))

# Changing a variable's value
student_age = 20
print("Updated age:", student_age)