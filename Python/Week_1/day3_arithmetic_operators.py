# Day 3 - Arithmetic Operators in Python

a = 10
b = 3

# Addition
print("Addition:", a + b)

# Subtraction
print("Subtraction:", a - b)

# Multiplication
print("Multiplication:", a * b)

# Division (always gives a decimal in Python)
print("Division:", a / b)

# Modulus (remainder after division)
print("Modulus:", a % b)

# Exponent (power) - Python only
print("Exponent:", a ** 2)   # 10^2 = 100